#!/bin/bash

for f in *.vcf; do
    IFS='/' read -r -a array <<< "$f"
	FILE=$f
	IFS='.' read -r -a array <<< "$FILE"
	PATIENT=${array[0]}
    echo $PATIENT
    grep -iE 'ASXL1|ATM|BAZ2A|BCOR|BIRC3|BRAF|BRCC3|BTK|CARD11|CD14|CHD2|CHEK2|DDX3X|DNMT3A|DYRK1A|EGR2|ELF4|EWSR1|FAM50A|FAT1|FBXW7|FUBP1|GNB1|HIST1H1B|HIST1H1E|IBTK|IGLL5|IKZF3|IRF4|ITPKB|KLHL6|KRAS|MAP2K1|MED12|MGA|MYD88|NFKBIE|NOTCH1|NRAS|NXF1|PIM1|PLCG2|POT1|PTPN11|RIPK1|RPS15|SAMHD1|SETD2|SF3B1|TP53|TRAF2|TRAF3|XP04|XPO1|ZMYM3|LTB|ZAP70|BCL2|SMARCA2|PRKD3|MAPK1|NKAP|CCND2|CDKN1B|ZNF292|ARID1A|SPEN|BAX|amp2p|amp8q|del6q21|del8p|del11q|del13q|del17p|del18p|del20p|tri12|#' $f > ${PATIENT}.cllgeneonly.vcf
done

